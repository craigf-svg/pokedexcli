const baseURL = "https://pokeapi.co/api/v2"

type Client struct {
	httpClient http.Client
	cache			 *pokecache.Cache
}

func NewClient(timeout time.Duration) *Client {
	return &Client{
		httpClient: http.Client{
			Timeout: timeout,
		},
		cache: pokecache.NewCache(5 * time.Second),
	}
}

